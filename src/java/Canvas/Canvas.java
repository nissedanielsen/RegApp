public class Canvas {
    
    String resultatid;
    String nameltu;
    String personnr;
    String modulnr;
    String betyg;

    public String getResultatid() {
        return resultatid;
    }

    public void setResultatid(String resultatid) {
        this.resultatid = resultatid;
    }

    public String getNameltu() {
        return nameltu;
    }

    public void setNameltu(String nameltu) {
        this.nameltu = nameltu;
    }

    public String getPersonnr() {
        return personnr;
    }

    public void setPersonnr(String personnr) {
        this.personnr = personnr;
    }

    public String getModulnr() {
        return modulnr;
    }

    public void setModulnr(String modulnr) {
        this.modulnr = modulnr;
    }

    public String getBetyg() {
        return betyg;
    }

    public void setBetyg(String betyg) {
        this.betyg = betyg;
    }
    

    
}
